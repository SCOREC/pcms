use, intrinsic :: ISO_C_BINDING
type(PcmsInterpolatorHandle) :: swig_result
type(PcmsInterpolatorOHMeshHandle), intent(in) :: oh_mesh
real(C_DOUBLE), intent(in) :: radius
type(SwigClassWrapper) :: fresult 
type(SwigClassWrapper) :: farg1 
real(C_DOUBLE) :: farg2 

farg1 = oh_mesh%swigdata
farg2 = radius
fresult = swigc_pcms_create_interpolator(farg1, farg2)
swig_result%swigdata = fresult
end function

subroutine pcms_destroy_interpolator(interpolator)
use, intrinsic :: ISO_C_BINDING
type(PcmsInterpolatorHandle), intent(in) :: interpolator
type(SwigClassWrapper) :: farg1 

farg1 = interpolator%swigdata
call swigc_pcms_destroy_interpolator(farg1)
end subroutine

